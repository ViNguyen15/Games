package janken;

public class Janken {
	private String rock;
	private String paper;
	private String scissor;
	
	public Janken() {
		rock = "rock";
		paper = "paper";
		scissor = "scissor";
	}
	
	public String getRock() {
		return rock;
	}
	
	public String getScissor() {
		return scissor;
	}
	
	public String getPaper() {
		return paper;
	}
	
	
}
